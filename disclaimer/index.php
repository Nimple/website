      <p>(1) Die Parteien (Nutzer und Appstronauten GbR) werden die jeweils anwendbaren, insbesondere die in Deutschland g&uuml;ltigen datenschutzrechtlichen Bestimmungen beachten und ihre im Zusammenhang mit dem Vertrag und dessen Durchf&uuml;hrung eingesetzten Besch&auml;ftigten auf das Datengeheimnis nach § 5 BDSG verpflichten, soweit diese nicht bereits allgemein entsprechend verpflichtet sind.<br>
