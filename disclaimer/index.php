      <h2>§ 2 Datenbehandlung bei Anhaltspunkten auf rechtswidrige Nutzung</h2>

      <p>Soweit tats&auml;chliche Anhaltspunkte bestehen, die zum Aufdecken sowie Unterbinden von rechtswidrigen Inanspruchnahmen der Dienstleistungen erforderlich sind, ist es gestattet zum Zweck der Durchsetzung eigener Anspr&uuml;che personenbezogene Daten zu erheben und zu verwenden.</p>

